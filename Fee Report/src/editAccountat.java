        {
            if(!emaild.getText().equals(""))
            {
                if(!pass.getText().equals(""))
                {
                    if(!contno.getText().equals(""))
                    {
                        try {
                          //  String sql1="UPDATE `accountat_info` SET `id`=[value-1],`name`=[value-2],`email`=[value-3],`password`=[value-4],`cont_no`=[value-5] WHERE 1"
                            String sql="update accountat_info set name='"+uname.getText()+"',password='"+pass.getText()+"',email='"+emaild.getText()+"',cont_no='"+contno.getText()+"' where id='"+jTextField1.getText()+"'";
                            PreparedStatement pst=conn.prepareStatement(sql);
                            int data=pst.executeUpdate();
                            if(data>0)
                            {
                                JOptionPane.showMessageDialog(null, "Updated Accountat Info");
                                adminDashboard adm=new adminDashboard();
                                adm.setVisible(true);
                                hide();
                            }   } catch (SQLException ex) {
                                Logger.getLogger(newAccountat.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, "Conatct No Required");
                        }
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "email id Required");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Password Required");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Username Required");
            }
