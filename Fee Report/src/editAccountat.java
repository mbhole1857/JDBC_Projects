    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        // TODO add your handling code here:
        String sql="select name,password,email,cont_no from accountat_info where id='"+jTextField1.getText()+"'";
        PreparedStatement pst=conn.prepareStatement(sql);
        ResultSet rst=pst.executeQuery();
        if(rst.next())
        {
            uname.setEnabled(true);
            uname.setText(rst.getString("name"));
        pass.setEnabled(true);
        pass.setText(rst.getString("password"));
        emaild.setEnabled(true);
        emaild.setText(rst.getString("email"));
        contno.setEnabled(true);
        contno.setText(rst.getString("cont_no"));
        jButton1.setEnabled(true);
        }
    } catch (SQLException ex) {
        Logger.getLogger(editAccountat.class.getName()).log(Level.SEVERE, null, ex);
    } 
    }//GEN-LAST:event_jButton3ActionPerformed

